import type { FingerNumber, Lesson, LessonStep, NoteDuration, PianoHand, PianoKeyName, StepNote } from '../types';

type StepOptions = Partial<Omit<LessonStep, 'text' | 'keys' | 'notes'>> & {
  fingers?: FingerNumber[];
  notes?: StepNote[];
};

const step = (text: string, keys: PianoKeyName[], options: StepOptions = {}): LessonStep => {
  const { fingers, notes, ...rest } = options;
  const duration = rest.duration ?? notes?.[0]?.duration ?? 'q';
  const hand = rest.hand ?? notes?.[0]?.hand;

  return {
    text,
    keys,
    expectedNote: rest.expectedNote ?? keys[0],
    notes:
      notes ??
      keys.map((key, index) => ({
        key,
        duration,
        finger: fingers?.[index],
        hand,
      })),
    ...rest,
    duration,
    hand,
  };
};

const note = (
  text: string,
  key: PianoKeyName,
  finger: FingerNumber,
  hand: PianoHand = 'right',
  options: StepOptions = {},
) => step(text, [key], { hand, fingers: [finger], ...options });

const chord = (
  text: string,
  keys: PianoKeyName[],
  fingers: FingerNumber[],
  hand: PianoHand = 'right',
  options: StepOptions = {},
) => step(text, keys, { hand, fingers, recognitionMode: 'chord', ...options });

const restCount = (count: string, duration: NoteDuration = 'q') =>
  step(`Tel ${count} in stilte en houd je hand ontspannen boven de toetsen.`, [], {
    duration,
    expectedNote: undefined,
    notes: [{ key: 'B3', duration, hand: 'right', rest: true }],
    scoreLabel: 'rust',
    recognitionMode: 'manual-score',
    coaching: 'Rust is ook muziek: blijf meetellen zonder een toets te spelen.',
  });

const cloneStepForPracticeRound = (source: LessonStep, roundIndex: number): LessonStep => {
  const roundLabels = [
    'Herhaal ontspannen',
    'Speel gelijkmatig',
    'Muzikale ronde',
    'Controleer je klank',
    'Laatste rustige ronde',
  ];
  const label = roundLabels[(roundIndex - 2) % roundLabels.length];

  return {
    ...source,
    text: `${label}: ${source.text}`,
    keys: [...source.keys],
    notes: source.notes?.map((noteItem) => ({ ...noteItem })),
  };
};

const targetStepCountFor = (lesson: Lesson) => {
  const moduleName = lesson.module.toLowerCase();
  const isRepertoire =
    lesson.source === 'traditional' ||
    lesson.source === 'public-domain' ||
    moduleName.includes('repertoire') ||
    moduleName.includes('klassieke') ||
    moduleName.includes('minimalistische');

  if (moduleName.includes('dagelijkse workouts')) {
    return 32;
  }

  if (isRepertoire) {
    return 48;
  }

  if (lesson.order <= 120) {
    return 32;
  }

  if (lesson.order <= 250) {
    return 36;
  }

  return 0;
};

const expandShortLesson = (lesson: Lesson): Lesson => {
  const targetStepCount = targetStepCountFor(lesson);

  if (targetStepCount === 0 || lesson.steps.length >= targetStepCount || lesson.steps.length === 0) {
    return {
      ...lesson,
      estimatedMinutes: Math.max(lesson.estimatedMinutes ?? 8, Math.ceil(lesson.steps.length * 0.42)),
    };
  }

  const expandedSteps = [...lesson.steps];
  let practiceIndex = 0;

  while (expandedSteps.length < targetStepCount) {
    const roundIndex = Math.floor(practiceIndex / lesson.steps.length) + 2;
    const sourceStep = lesson.steps[practiceIndex % lesson.steps.length];
    expandedSteps.push(cloneStepForPracticeRound(sourceStep, roundIndex));
    practiceIndex += 1;
  }

  return {
    ...lesson,
    estimatedMinutes: Math.max(lesson.estimatedMinutes ?? 8, Math.ceil(expandedSteps.length * 0.42)),
    steps: expandedSteps,
  };
};

